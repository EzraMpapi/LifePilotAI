const DICTS: Record<Lang, Dict> = { en, sw, fr };

export type TKey = keyof Dict;

const STORAGE_KEY = "app.language";

function detectDeviceLang(): Lang {
  try {
    const locales = Localization.getLocales();
    const code = locales?.[0]?.languageCode?.toLowerCase();
    if (code === "sw") return "sw";
    if (code === "fr") return "fr";
  } catch {
    // ignore
  }
  return "en";
}

type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TKey, vars?: Record<string, string | number>) => string;
  ready: boolean;
};

const I18nContext = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  const [ready, setReady] = useState(false);

  useEffect(() => {
    let alive = true;
    (async () => {
      const saved = (await storage.getItem(STORAGE_KEY)) as Lang | null;
      const initial = saved && DICTS[saved] ? saved : detectDeviceLang();
      if (alive) {
        setLangState(initial);
        setReady(true);
      }
    })();
    return () => { alive = false; };
  }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    storage.setItem(STORAGE_KEY, l);
  }, []);

  const t = useCallback(
    (key: TKey, vars?: Record<string, string | number>) => {
      const dict = DICTS[lang] ?? en;
      let str = (dict[key] ?? en[key] ?? key) as string;
      if (vars) {
        for (const k of Object.keys(vars)) {
          str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(vars[k]));
        }
      }
      return str;
    },
    [lang]
  );

  return (
    <I18nContext.Provider value={{ lang, setLang, t, ready }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}

// Convenience hook returning just the translate function.
export function useT() {
  return useI18n().t;
}
